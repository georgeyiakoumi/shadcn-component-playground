/** Advances past a string literal, handling escape characters. */
function skipString(source: string, start: number, quote: string): number {
  let i = start + 1
  while (i < source.length) {
    if (source[i] === "\\" && quote !== "`") {
      i += 2
      continue
    }
    if (source[i] === quote) return i + 1
    i++
  }
  return i
}
